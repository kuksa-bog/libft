#include "libft.h"

char	*ft_itoa(int n)
{
	int		j;
	int		z;
	int		i;
	char	*a;

	z = 1;
	i = 0;
	if (n < 0)
		i++;
	j = n;
	while ((j /= 10))
		z++;
	a = ft_strnew(z + i);
	if (a == NULL)
		return (NULL);
	if (i == 1)
		a[0] = '-';
	while (z--)
	{
		a[z + i] = (i ? -(n % 10) : (n % 10)) + '0';
		n = n / 10;
	}
	return (a);
}
