#include "libft.h"

int		ft_sqrt(int nb)
{
	int		i;
	int		q;
	int		s;

	i = nb;
	q = 1;
	s = 0;
	while (i >= 1 && q < 100000)
	{
		s = i / q;
		if (nb == s * s)
			return (s);
		q++;
	}
	return (0);
}
