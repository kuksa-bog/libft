#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*s;

	i = 0;
	if (!(s = (char*)malloc(sizeof(char) * ft_strlen(str) + 1)))
		return (NULL);
	while (str[i] != '\0')
	{
		s[i] = str[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
