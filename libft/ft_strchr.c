#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	int		c2;

	i = 0;
	c2 = c;
	while (s[i] != '\0')
		if (s[i++] == (c2 % 256))
			return ((char*)(s + i - 1));
	if (s[i] == (c2 % 256))
		return ((char*)(s + i));
	return (NULL);
}
